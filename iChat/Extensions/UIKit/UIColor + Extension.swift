import UIKit

extension UIColor {
    static func redButton() -> UIColor {
        return #colorLiteral(red: 0.8599731326, green: 0.1256897449, blue: 0.1353026032, alpha: 1)
    }
    static func darkButton() -> UIColor {
        return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }
    static func bottomBorderTextField() -> UIColor {
        return #colorLiteral(red: 0.7137254902, green: 0.7137254902, blue: 0.7137254902, alpha: 1)
    }
    
    static func shadowCell() -> UIColor {
        return #colorLiteral(red: 0.7137254902, green: 0.7137254902, blue: 0.7137254902, alpha: 1)
    }
}
